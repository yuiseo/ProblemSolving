def solution(n):
    # 1 => (1) 1개
    # 2 => (1,1),(2) 2개
    if n<3:
        return n
    
    # 3 => (1,1,1),(2,1),(1,2) 3개
    # 4 => (1,1,1,1),(1,2,1),(1,1,2),(2,1,1),(2,2) 5개
    else:
        dp=[0]*(n+1)
        dp[1],dp[2] = 1,2
        for i in range(3,n+1):
            dp[i]=dp[i-1]+dp[i-2]
        return dp[n]%1234567